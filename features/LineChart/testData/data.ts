const generate = (month: number, days: number) => {
  const monthInput = month < 10 ? `0${month}` : month;
  const data = [];
  for (let i = days; i > 0; i--) {
    const date = i < 10 ? `0${i}` : i;
    data.push({
      date: new Date(`2022-${monthInput}-${date}`),
      value: Math.floor(Math.random() * 10000)
    })
  }
  return data;
}

